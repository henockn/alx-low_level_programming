#include <unistd.h>
/**
 * _putchar - writes the given character
 * _isupper - determine whether a given character is in upper case
 * _isdigit - checks whether a given character is a digit
 * mul-find the multiplication of two numbers
 * print_numbers -print numbers from 0-9
 * print_most_numbers - print numbers from 0-9 except 2 and 4
 * more_numbers - print numbers from 0-14 10 times
 * print_line - print straight line of length n
 * print_diagonal - print diagonal line of length n
 * print_square - print square of length size
 * print_triangle - print triangle of length size
 */
int _putchar(char);
int _isupper(int c);
int _isdigit(int c);
int mul(int a, int b);
void print_numbers(void);
void print_most_numbers(void);
void more_numbers(void);
void print_line(int n);
void print_diagonal(int n);
void print_square(int size);
void print_triangle(int size);
